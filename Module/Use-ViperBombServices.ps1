			$tmp       = $_.StartType
			$StartType = If ( $tmp -eq 'Disabled' ) 
            { 
                1 
            } 

            ElseIf ( $tmp -eq 'Manual' )
            { 
                2 
            }
            
            ElseIf ( $tmp -eq 'Auto' )
            { 
                If ( AutoDelayTest $ServiceName -eq 1 )
                { 
                    4 
                } 
                
                Else
                { 
                    3
                }
			} 
            
            Else 
            {
				$tmp
			}

			If ( $ServiceName -Like "*$ServiceEnd" )
            { 
                $ServiceName = $ServiceName.Replace( '_.+' , '_?????' )
            }

			[ PSCustomObject ]@{ 

                ServiceName  = $ServiceName
                StartType    = $StartType
                Status       = $_.Status 
            }
		}
	}

	Return $TMPServiceL

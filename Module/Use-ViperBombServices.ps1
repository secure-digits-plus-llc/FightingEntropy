            $Script:csv    | Export-Csv -LiteralPath $TempSrv -Force -Delimiter ','
            $UpArg        += "-lcsc $TempSrv "
		}
	}

	$ArgList.ForEach{

        $TruCount          = 0
        
        If ( $GuiSwitch -and ! $_.Gui )
        { 
            $TC            = -1 
        } 
        
        Else
        { 
            $tmp           = $_.Var.Split('=')
            $Count         = $_.Match
            $TC            = $Count*2 
        }

		For ( $i = 0 ; $i -lt $TC ; $i += 2 )
        {
			$var           = GV -Name $tmp[$i] -ValueOnly
			
            If ( $var -eq $tmp[$i+1] )
            { 
                $TruCount++ 
            }
		}

		If ( $TruCount -eq $Count )
        { 
            $Script:Uparg += $_.Arg + " "
        }
	}
	
    If ( $ScriptLog -eq 1 )
    { 
        $UpArg            += "-logc $LogName " 
    }

	Clear-Host
	MenuLine -L
	MenuBlankLine -L

    $Splat    = @{ 
    
        Text  = ''.PadRight( 18 ) + 'Update Found!'
        Color = 13 
        Log   = 1 
    } 
    
    DisplayOutLML @Splat
	
    MenuBlankLine -L
	
    $Splat    = @{ 
    
        Text  = '|' , ' Updating from version ' , "$Script_Version".PadRight( 30 ) , '|'
        Color = 14 , 15 , 11 , 14 
        Log   = 1 
    }
    
    DisplayOut @Splat

	MenuBlankLine -L
	
    $Splat    = @{ 
    
        Text  = '|' , ' Downloading version ' , "$FullVer".PadRight( 31 ) , '|'
        Color = 14 , 15 , 11 , 14
        Log   = 1 
    } 
    
    DisplayOut @Splat
	
    $Splat    = @{ 
    
        Text  = "Will run after download is complete."
        Color = 15
        Log   = 1 
    } 
    
    DisplayOutLML @Splat

	MenuBlankLine -L
	MenuLine -L

	DownloadFile $Script_Url $ScrpFilePath
	SAPS Powershell "-NoProfile -ExecutionPolicy Bypass -File `"$ScrpFilePath`" $UpArg" -Verb RunAs
	CloseExit
}


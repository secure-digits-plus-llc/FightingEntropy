Function SaveSetting 
{
	UpdateSetting

	$Black_Viper = $WPF_ServiceConfig.SelectedIndex

	If ( ( $Black_Viper + 1 ) -eq $BVCount -or ( $IsLaptop -eq '-Lap' -and $LaptopTweaked -ne 1 -and $Black_Viper -ge 2 ) ) # <--- I would change this.
    { 

Function TOS 
{
	$Invalid = $False
	$CR      = $False
	
    While ( $TOS -ne 'Out' )
    {
		TOSDisplay -C:$CR
		
        $CR      = $False
		$Invalid = ShowInvalid $Invalid
		$TOS     = Read-Host "`nDo you Accept? (Y)es/(N)o"
		
        If ( $TOS -In 'n' , 'no' )
        {
			Exit
		} 

        ElseIf ( $TOS -In 'y' , 'yes' )
        {
			$TOS              = 'Out'
			$Script:AcceptToS = 'Accepted'
			$Script:RunScript = 1

            If ( $LoadServiceConfig -eq 1 )
            { 
                Black_Viper_Set 
            }
            
            ElseIf ( $Black_Viper -eq 0 )
            { 
                GuiStart 
            }
            
            Else
            { 
                Black_Viper_Set $Black_Viper $All_or_Min 
            }
		}

        ElseIf ( $TOS -eq 'l' )
        {
			$CR      = $True
            ShowCopyright
		} 
        
        Else 
        {
			$Invalid = $True
		}

	} 

    Return
}

